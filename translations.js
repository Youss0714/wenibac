// Function to get translation
function getTranslation(key, language = 'fr') {
    const keys = key.split('.');
    let translation = translations[language];
    
    for (const k of keys) {
        if (translation && typeof translation === 'object' && k in translation) {
            translation = translation[k];
        } else {
            // Fallback to French if translation not found
            if (language !== 'fr') {
                return getTranslation(key, 'fr');
            }
            console.warn(`Translation not found for key: ${key} in language: ${language}`);
            return null;
        }
    }
    
    return translation;
}

// Function to get all available languages
function getAvailableLanguages() {
    return Object.keys(translations);
}

// Function to add or update translations
function addTranslation(language, key, value) {
    if (!translations[language]) {
        translations[language] = {};
    }
    
    const keys = key.split('.');
    let current = translations[language];
    
    for (let i = 0; i < keys.length - 1; i++) {
        const k = keys[i];
        if (!current[k] || typeof current[k] !== 'object') {
            current[k] = {};
        }
        current = current[k];
    }
    
    current[keys[keys.length - 1]] = value;
}

// Function to validate translations completeness
function validateTranslations() {
    const languages = getAvailableLanguages();
    const issues = [];
    
    if (languages.length < 2) {
        return ['Not enough languages defined'];
    }
    
    const baseLanguage = languages[0];
    const baseKeys = getAllKeys(translations[baseLanguage]);
    
    for (let i = 1; i < languages.length; i++) {
        const lang = languages[i];
        const langKeys = getAllKeys(translations[lang]);
        
        // Check for missing keys
        const missingKeys = baseKeys.filter(key => !langKeys.includes(key));
        if (missingKeys.length > 0) {
            issues.push(`Missing keys in ${lang}: ${missingKeys.join(', ')}`);
        }
        
        // Check for extra keys
        const extraKeys = langKeys.filter(key => !baseKeys.includes(key));
        if (extraKeys.length > 0) {
            issues.push(`Extra keys in ${lang}: ${extraKeys.join(', ')}`);
        }
    }
    
    return issues;
}

// Helper function to get all keys from a nested object
function getAllKeys(obj, prefix = '') {
    let keys = [];
    
    for (const key in obj) {
        if (obj.hasOwnProperty(key)) {
            const fullKey = prefix ? `${prefix}.${key}` : key;
            
            if (typeof obj[key] === 'object' && obj[key] !== null) {
                keys = keys.concat(getAllKeys(obj[key], fullKey));
            } else {
                keys.push(fullKey);
            }
        }
    }
    
    return keys;
}

